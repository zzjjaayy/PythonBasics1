# While Loops
condition = True

while condition:
    print("jay")
    condition = False
# similar to while loops in other languages, the the lines indented{part of the block} will be
# executed as long as the condition is true.

i = 2
while i <= 4:
    print(i)
    i = i + 1
print("done")

# in Python, if you want to terminate the loop in between, use "break"
# While Loops in Python have an additional feature, while loops have else statments
# this means, if the while loop completes without breaking, the else statement will be executed.
