# ARITHMETIC OPERATIONS:

# NOTE : OPERATOR PRECEDENCE
# order of execution {parenthesis can change the order}
# 1. exponent
# 2. multiplication or division{both types} or modulus
# 3. addition or subtraction

print(10 + 3)  # addition = 13

print(10 - 3)  # subtraction = 7

print(10 * 3)  # multiplication = 30

print(10 / 3)  # division {returns a float} = 3.3333333333333

print(10 // 3)  # floor division {returns an int} = 3

print(10 % 3)  # modulus {returns the remainder} = 1

print(10 ** 3)  # exponent {to the power of} = 1000
